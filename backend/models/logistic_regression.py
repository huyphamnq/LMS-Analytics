# 9. HÀM DỰ ĐOÁN SINH VIÊN MỚI
# =================================================================
def predict_student(student_data: dict) -> dict:
    """
    Dự đoán nguy cơ học kém cho một sinh viên mới.

    Tham số:
        student_data: dict với key là tên feature, value là giá trị hành vi
                      Ví dụ: {'login_count_w1': 5, 'active_days_w1': 3, ...}

    Trả về:
        dict: {'label': 0 hoặc 1, 'probability': float, 'risk': str}
    """
    config  = joblib.load('model_config.pkl')
    _model  = joblib.load('logistic_student_model.pkl')
    _scaler = joblib.load('scaler_student.pkl')

    # Tạo vector feature đúng thứ tự, điền 0 nếu thiếu
    row = [student_data.get(f, 0) for f in config['features']]
    X   = np.array(row).reshape(1, -1)
    X_s = _scaler.transform(X)

    prob  = _model.predict_proba(X_s)[0, 1]
    label = int(prob >= config['threshold'])

    return {
        'label'      : label,
        'probability': round(prob, 4),
        'risk'       : 'KÉM (cần hỗ trợ)' if label == 1 else 'ĐẠT'
    }

