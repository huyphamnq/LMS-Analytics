sns.heatmap(cm_05, annot=True, fmt='d', cmap='YlGnBu',
            xticklabels=['Đạt', 'Kém'], yticklabels=['Đạt', 'Kém'])
plt.title('Ngưỡng mặc định (0.5)')
plt.xlabel('Dự đoán'); plt.ylabel('Thực tế')

plt.subplot(1, 2, 2)
cm_best = confusion_matrix(y_test, y_pred_best)
sns.heatmap(cm_best, annot=True, fmt='d', cmap='Oranges',
            xticklabels=['Đạt', 'Kém'], yticklabels=['Đạt', 'Kém'])
plt.title(f'Ngưỡng tối ưu ({best_threshold})')
plt.xlabel('Dự đoán'); plt.ylabel('Thực tế')

plt.suptitle('So sánh Ma trận nhầm lẫn: Ngưỡng 0.5 vs Tối ưu', fontsize=13, y=1.02)
plt.tight_layout()
plt.show()

# =================================================================
